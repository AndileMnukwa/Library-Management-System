    //    method to insert values into users table
    public void insertSignupDetails(){
    String name = txt_username.getText();
    String pwd = txt_password.getText();
    String email = txt_email.getText();
    String contact =  txt_contact.getText();
    
    try {
        Connection con = DBConnection.getConnection();
        String sql = "insert into users(username,password,email,contact) values(?,?,?,?)";
        PreparedStatement pat = con.prepareStatement(sql);
        
        
        pat.setString(1, name);
        pat.setString(2, pwd);
        pat.setString(3, email);
        pat.setString(4, contact);
        
        int updateRowCount = pat.executeUpdate();
        
        if (updateRowCount > 0) {
            JOptionPane.showMessageDialog(this, "Recorded Inserted Successfully");
        }
        else{
            JOptionPane.showMessageDialog(this, "Recorded Inserted Failure");
        }
        
    } catch (Exception e){
        e.printStackTrace();
    }
    }
